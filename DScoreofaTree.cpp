int dfs(int u, ll &ans, int p = -1)
{
    int ret = 0;
    for (auto v : adj[u])
        if (v != p)
            ret = max(ret, dfs(v, ans, u));
    ret++;
    ans = (ans + ret) % M;
    return ret;
}

